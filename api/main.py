@app.patch("/api/employees", response_model=BulkUpdateResponse)
def bulk_update_employees(req: BulkUpdateRequest):
    """
    Write pending cell edits back to planning.employees.
    Only fields marked editable=TRUE in planning.grid_columns are accepted.
    """
    if not req.updates:
        return BulkUpdateResponse(updated_rows=0, errors=[])

    conn = psycopg2.connect(DB_URL)
    cur  = conn.cursor()
    try:
        # Fetch the allowlist of editable field names from the grid config
        cur.execute("""
            SELECT gc.field_name
            FROM   planning.grid_columns gc
            JOIN   planning.grid_definitions gd ON gd.grid_id = gc.grid_id
            WHERE  gd.grid_name = 'Employees'
              AND  gc.editable  = TRUE
        """)
        editable_fields: set[str] = {row[0] for row in cur.fetchall()}

        updated_rows = 0
        errors: list[str] = []

        for item in req.updates:
            valid = {k: v for k, v in item.changes.items() if k in editable_fields}
            if not valid:
                errors.append(f"Row id={item.id}: no editable fields supplied (got: {list(item.changes.keys())})")
                continue

            set_clause = ", ".join(f'"{k}" = %s' for k in valid)
            values     = list(valid.values()) + [item.id]
            cur.execute(
                f'UPDATE planning.employees SET {set_clause} WHERE id = %s',
                values,
            )
            updated_rows += cur.rowcount

        conn.commit()
        return BulkUpdateResponse(updated_rows=updated_rows, errors=errors)

    except Exception as exc:
        conn.rollback()
        raise HTTPException(status_code=500, detail=str(exc))
    finally:
        cur.close()
        conn.close()


@app.get("/api/employees", response_model=EmployeesResponse)
def get_employees():
    """Return all rows from planning.employees."""
    try:
        columns, rows, col_types = run_query("SELECT * FROM planning.employees ORDER BY 1")
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc))
    return EmployeesResponse(columns=columns, column_types=col_types, rows=rows)


@app.get("/api/grid-config", response_model=GridConfigResponse)
def get_grid_config(grid_name: str = "Employees", role: str = "viewer"):
    """
    Return the merged grid configuration for a given grid name and role.
    Runs 3 queries:
      1. grid_definitions LEFT JOIN grid_role_layout  (role-aware layout)
      2. grid_columns      LEFT JOIN grid_column_permissions (role-aware column access)
      3. grid_format_rules JOIN grid_columns           (conditional cell styles)
    """
    conn = psycopg2.connect(DB_URL)
    cur  = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)

    try:
        # ── Query 1: grid + role layout + primary-key field ──────────────────
        # The PK column name is looked up from information_schema so that the
        # frontend can identify each row generically (e.g. "id" for employees,
        # "contact_id" for contacts) without any hardcoded assumptions.
        cur.execute("""
            SELECT
                gd.grid_id,
                gd.grid_name,
                gd.description,
                gd.dataset_table,
                gd.pagination_enabled,
                COALESCE(grl.page_size,      gd.page_size)      AS page_size,
                COALESCE(grl.frozen_columns, gd.frozen_columns) AS frozen_columns,
                gd.frozen_rows,
                COALESCE(grl.allow_export, TRUE)                AS allow_export,
                COALESCE(grl.allow_edit,   FALSE)               AS allow_edit,
                COALESCE(kcu.column_name,  'id')                AS primary_key_field
            FROM  planning.grid_definitions  gd
            LEFT JOIN planning.grid_role_layout grl
                   ON  grl.grid_id   = gd.grid_id
                   AND grl.role_name = %s
            -- Derive the PK column name from information_schema (first ordinal position)
            LEFT JOIN information_schema.table_constraints tc
                   ON  tc.table_schema     = 'planning'
                   AND tc.table_name       = SPLIT_PART(COALESCE(gd.dataset_table, ''), '.', 2)
                   AND tc.constraint_type  = 'PRIMARY KEY'
            LEFT JOIN information_schema.key_column_usage kcu
                   ON  kcu.constraint_name = tc.constraint_name
                   AND kcu.table_schema    = tc.table_schema
                   AND kcu.ordinal_position = 1
            WHERE gd.grid_name = %s
        """, (role, grid_name))
        grid_row = cur.fetchone()
        if not grid_row:
            raise HTTPException(status_code=404, detail=f"Grid '{grid_name}' not found.")
        grid_id = grid_row["grid_id"]

        # ── Query 2: columns + role permissions ──────────────────────────────
        cur.execute("""
            SELECT
                gc.column_id,
                gc.field_name,
                gc.header_name,
                gc.data_type,
                gc.sortable,
                gc.filterable,
                gc.editable,
                gc.visible,
                gc.pinned,
                gc.column_order,
                COALESCE(gcp.can_view, TRUE)  AS can_view,
                COALESCE(gcp.can_edit, FALSE) AS can_edit
            FROM  planning.grid_columns gc
            LEFT JOIN planning.grid_column_permissions gcp
                   ON  gcp.column_id  = gc.column_id
                   AND gcp.role_name  = %s
            WHERE gc.grid_id = %s
            ORDER BY gc.column_order
        """, (role, grid_id))
        col_rows = cur.fetchall()

        # ── Query 3: format rules ─────────────────────────────────────────────
        cur.execute("""
            SELECT
                gc.field_name,
                gfr.condition_expression,
                gfr.style_json,
                gfr.priority
            FROM  planning.grid_format_rules gfr
            JOIN  planning.grid_columns gc
                   ON gc.column_id = gfr.column_id
            WHERE gc.grid_id = %s
            ORDER BY gc.field_name, gfr.priority ASC
        """, (grid_id,))
        rule_rows = cur.fetchall()

    finally:
        cur.close()
        conn.close()

    return GridConfigResponse(
        grid=GridDefinitionOut(**dict(grid_row)),
        columns=[GridColumnOut(**dict(r)) for r in col_rows],
        format_rules=[
            GridFormatRuleOut(
                field_name=r["field_name"],
                condition_expression=r["condition_expression"],
                style_json=dict(r["style_json"]) if r["style_json"] else {},
                priority=r["priority"],
            )
            for r in rule_rows
        ],
    )


