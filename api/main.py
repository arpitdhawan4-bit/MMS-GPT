@app.get("/api/grid-data", response_model=EmployeesResponse)
def get_grid_data(grid_name: str = "Employees"):
    """
    Return all rows from the dataset_table configured for the given grid.
    Looks up planning.grid_definitions.dataset_table, then runs
      SELECT * FROM <dataset_table> ORDER BY 1
    Supports any grid (Employees, Contacts, or any future grid) — no
    per-table endpoint needed.
    """
    conn = psycopg2.connect(DB_URL)
    cur  = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)
    try:
        cur.execute(
            "SELECT dataset_table FROM planning.grid_definitions WHERE grid_name = %s",
            (grid_name,)
        )
        row = cur.fetchone()
    finally:
        cur.close()
        conn.close()

    if not row:
        raise HTTPException(status_code=404, detail=f"Grid '{grid_name}' not found.")
    dataset_table = row["dataset_table"]
    if not dataset_table:
        raise HTTPException(status_code=400, detail=f"Grid '{grid_name}' has no dataset_table configured.")
    if not dataset_table.startswith("planning."):
        raise HTTPException(status_code=400, detail="Only planning.* tables are supported.")

    try:
        columns, rows, col_types = run_query(f"SELECT * FROM {dataset_table} ORDER BY 1")
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc))

    return EmployeesResponse(columns=columns, column_types=col_types, rows=rows)


@app.patch("/api/grid-data", response_model=BulkUpdateResponse)
def bulk_update_grid_data(grid_name: str, req: BulkUpdateRequest):
    """
    Write pending cell edits back to the dataset_table for the given grid.
    - Looks up the editable columns from planning.grid_columns.
    - Looks up the PK column name from information_schema.
    - Only fields marked editable=TRUE are accepted (SQL-injection-safe allowlist).
    Works for any grid (Employees, Contacts, etc.) without per-table code.
    """
    if not req.updates:
        return BulkUpdateResponse(updated_rows=0, errors=[])

    conn = psycopg2.connect(DB_URL)
    cur  = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)
    try:
        # ── Look up dataset_table + editable columns ──────────────────────────
        cur.execute("""
            SELECT gd.dataset_table, gc.field_name
            FROM   planning.grid_definitions gd
            JOIN   planning.grid_columns gc ON gc.grid_id = gd.grid_id
            WHERE  gd.grid_name  = %s
              AND  gc.editable   = TRUE
        """, (grid_name,))
        results = cur.fetchall()
        if not results:
            raise HTTPException(
                status_code=404,
                detail=f"Grid '{grid_name}' not found or has no editable columns."
            )

        dataset_table    = results[0]["dataset_table"]
        editable_fields  = {r["field_name"] for r in results}

        if not dataset_table:
            raise HTTPException(status_code=400, detail=f"Grid '{grid_name}' has no dataset_table configured.")
        if not dataset_table.startswith("planning."):
            raise HTTPException(status_code=400, detail="Only planning.* tables are supported.")

        # ── Look up the actual primary-key column name ────────────────────────
        table_name = dataset_table.split(".", 1)[1]   # e.g. "employees" or "contacts"
        cur.execute("""
            SELECT kcu.column_name
            FROM   information_schema.table_constraints tc
            JOIN   information_schema.key_column_usage kcu
                   ON  kcu.constraint_name  = tc.constraint_name
                   AND kcu.table_schema     = tc.table_schema
            WHERE  tc.table_schema      = 'planning'
              AND  tc.table_name        = %s
              AND  tc.constraint_type   = 'PRIMARY KEY'
            ORDER  BY kcu.ordinal_position
            LIMIT  1
        """, (table_name,))
        pk_row = cur.fetchone()
        pk_col = pk_row["column_name"] if pk_row else "id"

        # ── Apply updates ─────────────────────────────────────────────────────
        # Switch to regular cursor for DML (RealDictCursor not needed)
        cur.close()
        cur = conn.cursor()

        updated_rows = 0
        errors: list[str] = []

        for item in req.updates:
            valid = {k: v for k, v in item.changes.items() if k in editable_fields}
            if not valid:
                errors.append(
                    f"Row pk={item.id}: no editable fields supplied "
                    f"(got: {list(item.changes.keys())})"
                )
                continue

            set_clause = ", ".join(f'"{k}" = %s' for k in valid)
            values     = list(valid.values()) + [item.id]
            cur.execute(
                f'UPDATE {dataset_table} SET {set_clause} WHERE "{pk_col}" = %s',
                values,
            )
            updated_rows += cur.rowcount

        conn.commit()
        return BulkUpdateResponse(updated_rows=updated_rows, errors=errors)

    except HTTPException:
        raise
    except Exception as exc:
        conn.rollback()
        raise HTTPException(status_code=500, detail=str(exc))
    finally:
        cur.close()
        conn.close()


@app.post("/api/paginate", response_model=PaginateResponse)
def paginate(req: PaginateRequest):
    """
    Execute a pre-validated base SQL query with LIMIT / OFFSET for server-side
    pagination.  Only called by the frontend when is_large_result = True.
    """
    clean = req.sql.strip()

    # Basic security: must be a read-only SELECT or WITH (CTE) query
    if not re.match(r"^\s*(SELECT|WITH)\b", clean, re.IGNORECASE):
        raise HTTPException(status_code=400, detail="Only SELECT queries are allowed.")
    if _FORBIDDEN_SQL.search(clean):
        raise HTTPException(status_code=400, detail="Forbidden SQL operation detected.")
    if not (1 <= req.limit <= 500):
        raise HTTPException(status_code=400, detail="limit must be between 1 and 500.")
    if req.offset < 0:
        raise HTTPException(status_code=400, detail="offset must be >= 0.")

    paginated_sql = (
        f"SELECT * FROM ({clean}) AS _paged "
        f"LIMIT {req.limit} OFFSET {req.offset}"
    )
    try:
        columns, rows, col_types = run_query(paginated_sql)
    except Exception as exc:
        raise HTTPException(status_code=500, detail=str(exc))

    return PaginateResponse(columns=columns, column_types=col_types, rows=rows)
